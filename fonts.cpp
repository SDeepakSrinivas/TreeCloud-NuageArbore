int str_width(unichar *st,int font,int fontsize){
//Prend une chaine st, de taille fontsize et renvoie le nombre de pixel que sa prendra � l'affichage
//font=0 : Times New Romant
//font=1 : Arial
//font=2 : Courrier
	double width=0;
	unichar c;
	unichar *p=st;
	if (font>2 || font<0)
	{
		printf("invalid font, must be 0,1 or 2\n");
		return 0;
	}
	if (font==0)
	{
		while ((c=*p++))
			width+=timesFontWidth[c];
	}
	if (font==1)
	{
		while ((c=*p++))
			width+=arialFontWidth[c];
	}
	if (font==2)
	{
		while ((c=*p++))
			width+=courFontWidth[c];
	}
	width*=fontsize;
	return ceil(width);
}

